#import "CG3DPoint.h"

@implementation CG3DPoint

- (instancetype)initWithX:(CGFloat)x Y:(CGFloat)y Z:(CGFloat)z
{
    self = [super init];
    self.x = x;
    self.y = y;
    self.z = z;
    
    return self;
}
@end
